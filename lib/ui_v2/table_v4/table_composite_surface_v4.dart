    if (entry is String) {
      final int? parsed = int.tryParse(entry);
      if (parsed != null) {
        return Color(parsed);
      }
    }
    return null;
  }

  static String? _readString(Map<String, Object?> source, List<String> keys) {
    for (final String key in keys) {
      final Object? entry = source[key];
      if (entry is String && entry.isNotEmpty) {
        return entry;
      }
    }
    return null;
  }

