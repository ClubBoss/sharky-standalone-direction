    final Map<String, Object> activationFrameMap =
        activationFrame is Map<String, Object>
        ? activationFrame as Map<String, Object>
        : <String, Object>{};
    final Map<String, Object> materialization = <String, Object>{}
      ..addEntries(
        _materializationBridge
            .asReadOnlyMap()
            .entries
            .where((entry) => entry.value != null)
            .map((entry) => MapEntry(entry.key, entry.value!)),
      );
