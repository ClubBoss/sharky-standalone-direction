  static String _normalizeMood(Object? raw) {
    final String value = (raw as String? ?? '').toLowerCase();
    const Set<String> valid = <String>{'calm', 'focus', 'tension', 'tilt'};
    return valid.contains(value) ? value : 'none';
  }

  static int _normalizeIntensity(Object? raw) {
    if (raw is int) return raw.clamp(0, 5);
    if (raw is num) return raw.toInt().clamp(0, 5);
    if (raw is String) {
      final int? parsed = int.tryParse(raw);
      if (parsed != null) return parsed.clamp(0, 5);
    }
    return 1;
  }

