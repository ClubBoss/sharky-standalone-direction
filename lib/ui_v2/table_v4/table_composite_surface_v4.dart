  static FontWeight _fontWeightFromEntry(Object? entry) {
    if (entry is FontWeight) {
      return entry;
    }
    if (entry is num) {
      return _fontWeightFromValue(entry.toInt());
    }
    if (entry is String) {
      final int? parsed = int.tryParse(entry);
      if (parsed != null) {
        return _fontWeightFromValue(parsed);
      }
      return _fontWeightFromName(entry);
    }
    return FontWeight.normal;
  }

  static FontWeight _fontWeightFromValue(int value) {
    final int normalized = ((value + 50) ~/ 100 * 100).clamp(100, 900);
    switch (normalized) {
      case 100:
        return FontWeight.w100;
      case 200:
        return FontWeight.w200;
      case 300:
        return FontWeight.w300;
      case 400:
        return FontWeight.w400;
      case 500:
        return FontWeight.w500;
      case 600:
        return FontWeight.w600;
      case 700:
        return FontWeight.w700;
      case 800:
        return FontWeight.w800;
      case 900:
        return FontWeight.w900;
      default:
        return FontWeight.normal;
    }
  }

  static FontWeight _fontWeightFromName(String name) {
    final String normalized = name.toLowerCase();
    if (normalized.contains('thin')) {
      return FontWeight.w100;
    }
    if (normalized.contains('extra_light') ||
        normalized.contains('extralight') ||
        normalized.contains('ultralight')) {
      return FontWeight.w200;
    }
    if (normalized.contains('light')) {
      return FontWeight.w300;
    }
    if (normalized.contains('medium')) {
      return FontWeight.w500;
    }
    if (normalized.contains('semibold') ||
        normalized.contains('semi_bold') ||
        normalized.contains('demibold')) {
      return FontWeight.w600;
    }
    if (normalized.contains('bold')) {
      return FontWeight.w700;
    }
    if (normalized.contains('extra_bold') ||
        normalized.contains('extrabold') ||
        normalized.contains('heavy')) {
      return FontWeight.w800;
    }
    if (normalized.contains('black')) {
      return FontWeight.w900;
    }
    return FontWeight.normal;
  }

  static Color? _colorFromEntry(Object? entry) {
    if (entry is Color) {
